#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	if (!dst && !src)
		return (0);
	d = dst;
	s = (unsigned char *)src;
	while (len--)
		*d++ = *s++;
	return (dst);
}
